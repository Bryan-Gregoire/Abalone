#ifndef COLORBOARD_H
#define COLORBOARD_H
#include <ostream>

namespace abalone {
    enum CodeBoard {
        FG_RED      = 31,
        FG_GREEN    = 32,
        FG_BLUE     = 34,
        FG_DEFAULT  = 39,
        BG_RED      = 41,
        BG_GREEN    = 42,
        BG_BLUE     = 44,
        BG_DEFAULT  = 49
    };
    class Modifier {
        CodeBoard code;
    public:
        Modifier(CodeBoard pCode) : code(pCode) {}
        friend std::ostream&
        operator<<(std::ostream& os, const Modifier& mod) {
            return os << "\033[" << mod.code << "m";
        }
    };
}
#endif // COLORBOARD_H

